using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class SquareTilePlacement : MonoBehaviour
{
    public SquareTile assignTile(int tileType){
        if(tileType == 0){
            sides = [0,1,0,0];
        }
        else if(tileType == 1){
            sides = [0,1,0,0];
        }
    }
}
